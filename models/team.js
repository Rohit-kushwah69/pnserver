const mongoose = require('mongoose')

const teamSchema = mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    image: {
        public_id: {
            type: String,
            required: true
        },
        url: {
            type: String,
            required: true
        }
    },
}, { timestamps: true });

const teamModel = mongoose.model('team', teamSchema)
module.exports = teamModel