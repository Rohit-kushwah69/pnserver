const mongoose = require('mongoose')

const studentSchema = mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    image: {
        public_id: {
            type: String,
            required: true
        },
        url: {
            type: String,
            required: true
        }
    },
}, { timestamps: true });

const studentModel = mongoose.model('student', studentSchema)
module.exports = studentModel